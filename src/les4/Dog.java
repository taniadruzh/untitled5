package les4;

public class Dog {
    int colPaws;
    boolean hungry;
    String name;

    public int getColPaws() {
        return colPaws;
    }

    public boolean getHungry() {
        return hungry;
    }

    public String getName() {
        return name;
    }

    public void setColPaws(int colPaws) {
        this.colPaws = colPaws;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setHungry(boolean hungry) {
        this.hungry = hungry;
    }
}
