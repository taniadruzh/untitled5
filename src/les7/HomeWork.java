package les7;

public class HomeWork
{
  private int countHours;
  private int countWorks;
  
  public HomeWork(){}
  
  public HomeWork(int countHours, int countWorks)
  {
    this.countHours = countHours;
    this.countWorks = countWorks;
  }
  
  public void setCountHours(int countHours)
  {
    this.countHours = countHours;
  }
  
  public void setCountWorks(int countWorks)
  {
    this.countWorks = countWorks;
  }
  
   public int getCountHours()
  {
    return countHours;
  }
  
  public int setCountWorks()
  {
    return countWorks;
  }  
  
}
